str = input("Enter any Strings : ")

# for Upper Case in Python
upp = str.upper()
print(upp)
print()

# for Lower Case in Python
lower = str. lower()
print(lower)
print()

# for Swap Case in Python
swap = str.swapcase()
print(swap)
print()

# for title Case in Python
title = str.title()
print(title)

